#!/usr/bin/env node

const fs = require('fs').promises;
const fsSync = require('fs');
const path = require('path');
const { execSync, exec } = require('child_process');
const readline = require('readline');
const os = require('os');
const { promisify } = require('util');
const execAsync = promisify(exec);

const CONFIG_FILE = path.join(process.cwd(), 'dados', 'src', 'config.json');
const isWindows = os.platform() === 'win32';

let version = 'Desconhecida';
try {
  const packageJson = JSON.parse(fsSync.readFileSync(path.join(process.cwd(), 'package.json'), 'utf8'));
  version = packageJson.version;
} catch (error) {
}

const colors = {
  reset: '\x1b[0m',
  green: '\x1b[1;32m',
  red: '\x1b[1;31m',
  blue: '\x1b[1;34m',
  yellow: '\x1b[1;33m',
  cyan: '\x1b[1;36m',
  magenta: '\x1b[1;35m',
  dim: '\x1b[2m',
  bold: '\x1b[1m',
  underline: '\x1b[4m',
};

function printMessage(text) {
  console.log(`${colors.green}${text}${colors.reset}`);
}

function printWarning(text) {
  console.log(`${colors.red}${text}${colors.reset}`);
}

function printInfo(text) {
  console.log(`${colors.cyan}${text}${colors.reset}`);
}

function printDetail(text) {
  console.log(`${colors.dim}${text}${colors.reset}`);
}

function printSeparator() {
  console.log(`${colors.blue}============================================${colors.reset}`);
}

function validateInput(input, field) {
  switch (field) {
    case 'prefixo':
      if (input.length !== 1) {
        printWarning('‚ö†Ô∏è O prefixo deve ter exatamente 1 caractere.');
        return false;
      }
      return true;

    case 'numero':
      if (!/^[0-9]{10,15}$/.test(input)) {
        printWarning('‚ö†Ô∏è N√∫mero inv√°lido! Deve conter apenas d√≠gitos (10 a 15).');
        printDetail('üìù Exemplo: 5511999999999');
        return false;
      }
      return true;

    default:
      return true;
  }
}

function setupGracefulShutdown() {
  const shutdown = () => {
    console.log('\n');
    printWarning('üõë Configura√ß√£o cancelada pelo usu√°rio.');
    process.exit(0);
  };

  process.on('SIGINT', shutdown);
  process.on('SIGTERM', shutdown);
}

async function isTermux() {
  try {
    return !!process.env.TERMUX_VERSION || (await execAsync('command -v pkg')).stdout.includes('termux');
  } catch {
    return false;
  }
}

async function installFFmpeg() {
  printSeparator();
  printMessage('üì¶ Instalando FFmpeg e ffprobe...');

  try {
    const platform = os.platform();
    let command;

    if (await isTermux()) {
      try {
        await execAsync('command -v pkg');
        command = 'pkg install ffmpeg -y';
      } catch {
        printWarning('‚ö†Ô∏è Termux detectado, mas pkg n√£o encontrado.');
        printInfo('üìù Instale o FFmpeg e ffprobe manualmente no Termux: pkg install ffmpeg');
        return;
      }
    } else if (platform === 'linux') {
      try {
        await execAsync('command -v apt-get');
        command = 'sudo apt-get update && sudo apt-get install -y ffmpeg';
      } catch {
        try {
          await execAsync('command -v yum');
          command = 'sudo yum install -y ffmpeg';
        } catch {
          try {
            await execAsync('command -v dnf');
            command = 'sudo dnf install -y ffmpeg';
          } catch {
            printWarning('‚ö†Ô∏è Nenhum gerenciador de pacotes compat√≠vel (apt/yum/dnf) encontrado.');
            printInfo('üìù Instale o FFmpeg e ffprobe manualmente: https://ffmpeg.org/download.html');
            return;
          }
        }
      }
    } else if (platform === 'darwin') {
      try {
        await execAsync('command -v brew');
        command = 'brew install ffmpeg';
      } catch {
        printWarning('‚ö†Ô∏è Homebrew n√£o encontrado. Instale o Homebrew primeiro: https://brew.sh');
        printInfo('üìù Ap√≥s instalar o Homebrew, execute: brew install ffmpeg');
        return;
      }
    } else if (platform === 'win32') {
      try {
        await execAsync('winget --version');
        command = 'winget install --id Gyan.FFmpeg -e';
      } catch {
        printWarning('‚ö†Ô∏è winget n√£o encontrado ou FFmpeg/ffprobe n√£o instalado.');
        printInfo('üìù Baixe e instale o FFmpeg (inclui ffprobe) manualmente: https://ffmpeg.org/download.html');
        printDetail('üìå Adicione o FFmpeg ao PATH do sistema ap√≥s a instala√ß√£o.');
        return;
      }
    } else {
      printWarning('‚ö†Ô∏è Sistema operacional n√£o suportado para instala√ß√£o autom√°tica do FFmpeg/ffprobe.');
      printInfo('üìù Instale o FFmpeg e ffprobe manualmente: https://ffmpeg.org/download.html');
      return;
    }

    await new Promise((resolve, reject) => {
      const ffmpegProcess = exec(command, { shell: isWindows }, (error) =>
        error ? reject(error) : resolve()
      );

      const spinner = ['‚†ã', '‚†ô', '‚†π', '‚†∏', '‚†º', '‚†¥', '‚†¶', '‚†ß', '‚†á', '‚†è'];
      let i = 0;
      const interval = setInterval(() => {
        process.stdout.write(`\r${spinner[i]} Instalando FFmpeg e ffprobe...`);
        i = (i + 1) % spinner.length;
      }, 100);

      ffmpegProcess.on('close', () => {
        clearInterval(interval);
        process.stdout.write('\r                                \r');
      });
    });

    try {
      await execAsync('ffmpeg -version');
      printMessage('‚úÖ FFmpeg instalado com sucesso.');
    } catch {
      printWarning('‚ö†Ô∏è FFmpeg instalado, mas n√£o encontrado no PATH.');
      printInfo('üìù Certifique-se de que o FFmpeg est√° no PATH do sistema ou no ambiente Termux.');
    }

    try {
      await execAsync('ffprobe -version');
      printMessage('‚úÖ ffprobe instalado com sucesso.');
    } catch {
      printWarning('‚ö†Ô∏è ffprobe instalado, mas n√£o encontrado no PATH.');
      printInfo('üìù Certifique-se de que o ffprobe est√° no PATH do sistema ou no ambiente Termux.');
      printDetail('üìå ffprobe geralmente vem com o FFmpeg. Verifique a instala√ß√£o do FFmpeg.');
    }
  } catch (error) {
    printWarning(`‚ùå Erro ao instalar FFmpeg/ffprobe: ${error.message}`);
    printInfo('üìù Instale o FFmpeg e ffprobe manualmente: https://ffmpeg.org/download.html');
  }
}

async function installDependencies() {
  printSeparator();
  printMessage('üì¶ Instalando depend√™ncias do Node.js...');

  try {
    await new Promise((resolve, reject) => {
      const npmProcess = exec('npm install --no-optional --force --no-bin-links', { shell: isWindows }, (error) =>
        error ? reject(error) : resolve()
      );

      const spinner = ['‚†ã', '‚†ô', '‚†π', '‚†∏', '‚†º', '‚†¥', '‚†¶', '‚†ß', '‚†á', '‚†è'];
      let i = 0;
      const interval = setInterval(() => {
        process.stdout.write(`\r${spinner[i]} Instalando depend√™ncias...`);
        i = (i + 1) % spinner.length;
      }, 100);

      npmProcess.on('close', () => {
        clearInterval(interval);
        process.stdout.write('\r                                \r');
      });
    });

    printMessage('‚úÖ Depend√™ncias do Node.js instaladas com sucesso.');
  } catch (error) {
    printWarning(`‚ùå Erro ao instalar depend√™ncias do Node.js: ${error.message}`);
    printInfo('üìù Tente executar manualmente: npm run config:install');
    process.exit(1);
  }

  await installFFmpeg();
}

async function checkSystemRequirements() {
  printSeparator();
  printMessage('üîç Verificando requisitos do sistema...');

  try {
    const nodeVersion = await execAsync('node --version');
    printDetail(`‚úÖ Node.js encontrado: ${nodeVersion.stdout.trim()}`);
  } catch {
    printWarning('‚ö†Ô∏è Node.js n√£o encontrado. Instale o Node.js: https://nodejs.org ou no Termux: pkg install nodejs');
    process.exit(1);
  }

  try {
    const npmVersion = await execAsync('npm --version');
    printDetail(`‚úÖ npm encontrado: ${npmVersion.stdout.trim()}`);
  } catch {
    printWarning('‚ö†Ô∏è npm n√£o encontrado. Instale o Node.js com npm: https://nodejs.org ou no Termux: pkg install nodejs');
    process.exit(1);
  }

  if (await isTermux()) {
    printDetail('‚úÖ Ambiente Termux detectado.');
  }

  printMessage('‚úÖ Todos os requisitos do sistema verificados.');
}

async function displayHeader() {
  const header = [
    `${colors.bold}üöÄ Configurador do Richbess - Vers√£o ${version}${colors.reset}`,
    `${colors.bold}üë®‚Äçüíª Criado por Hiudy${colors.reset}`,
  ];

  printSeparator();
  for (const line of header) {
    await new Promise((resolve) => {
      process.stdout.write(line + '\n');
      setTimeout(resolve, 100);
    });
  }
  printSeparator();
  console.log();
}

async function main() {
  try {
    setupGracefulShutdown();

    if (process.argv.includes('--install')) {
      await checkSystemRequirements();
      await installDependencies();
      process.exit(0);
    }

    await displayHeader();
    await checkSystemRequirements();

    const defaultConfig = {
      nomedono: '',
      numerodono: '',
      nomebot: '',
      prefixo: '!',
      aviso: false,
      debug: false,
      enablePanel: false,
    };

    let config = { ...defaultConfig };

    try {
      if (fsSync.existsSync(CONFIG_FILE)) {
        const existingConfig = JSON.parse(await fs.readFile(CONFIG_FILE, 'utf8'));
        config = { ...config, ...existingConfig };
        printInfo('üìÇ Configura√ß√£o existente carregada.');
      }
    } catch (error) {
      printWarning(`‚ö†Ô∏è Erro ao ler config.json: ${error.message}`);
      printInfo('üìù Usando valores padr√£o.');
    }

    const rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout,
    });

    printInfo(`${colors.bold}${colors.underline}üîß Configura√ß√µes B√°sicas${colors.reset}`);
    config.nomedono = await promptInput(rl, 'üë§ Nome do dono do bot', config.nomedono);
    config.numerodono = await promptInput(rl, 'üì± N√∫mero do dono (com DDD, apenas d√≠gitos)', config.numerodono, 'numero');
    config.nomebot = await promptInput(rl, 'ü§ñ Nome do bot', config.nomebot);
    config.prefixo = await promptInput(rl, 'üî£ Prefixo do bot (1 caractere)', config.prefixo, 'prefixo');

    config.aviso = false;
    config.debug = false;
    config.enablePanel = false;

    try {
      const configDir = path.dirname(CONFIG_FILE);
      if (!fsSync.existsSync(configDir)) {
        await fs.mkdir(configDir, { recursive: true });
      }

      await fs.writeFile(CONFIG_FILE, JSON.stringify(config, null, 2));

      console.log();
      printInfo('üìã Resumo da Configura√ß√£o');
      printDetail(`üë§ Nome do dono: ${config.nomedono}`);
      printDetail(`üì± N√∫mero do dono: ${config.numerodono}`);
      printDetail(`ü§ñ Nome do bot: ${config.nomebot}`);
      printDetail(`üî£ Prefixo: ${config.prefixo}`);

      printSeparator();
      printMessage('‚úÖ Configura√ß√£o salva com sucesso em config.json!');
      printSeparator();

      const installNow = await confirm(rl, 'üì¶ Deseja instalar as depend√™ncias, FFmpeg e ffprobe agora?', 's');

      if (installNow) {
        rl.close();
        await installDependencies();
      } else {
        printMessage('üìù Voc√™ pode instalar as depend√™ncias, FFmpeg e ffprobe depois com: npm run config:install');
      }

      printSeparator();
      printMessage(`üéâ Richbess configurado e pronto para uso! Vers√£o: ${version}`);
      printSeparator();
    } catch (error) {
      printWarning(`‚ùå Erro ao salvar configura√ß√£o: ${error.message}`);
    }

    rl.close();
  } catch (error) {
    printWarning(`‚ùå Erro inesperado: ${error.message}`);
    process.exit(1);
  }
}

async function promptInput(rl, prompt, defaultValue, field = null) {
  return new Promise((resolve) => {
    const displayPrompt = `${prompt} ${colors.dim}(atual: ${defaultValue})${colors.reset}: `;
    console.log(displayPrompt);
    rl.question("-->", async (input) => {
      const value = input.trim() || defaultValue;

      if (field && !validateInput(value, field)) {
        return resolve(await promptInput(rl, prompt, defaultValue, field));
      }

      resolve(value);
    });
  });
}

async function confirm(rl, prompt, defaultValue = 'n') {
  return new Promise((resolve) => {
    const defaultText = defaultValue.toLowerCase() === 's' ? 'S/n' : 's/N';
    console.log(`${prompt} (${defaultText}): `);
    rl.question("-->", (input) => {
      const response = (input.trim() || defaultValue).toLowerCase();
      resolve(response === 's' || response === 'sim' || response === 'y' || response === 'yes');
    });
  });
}

main().catch((error) => {
  printWarning(`‚ùå Erro fatal: ${error.message}`);
  process.exit(1);
});